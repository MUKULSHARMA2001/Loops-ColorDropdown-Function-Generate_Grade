//for loop code to display 1 t0 10 numbers
var i;
document.write("<h1>PRINT 1 T0 10 NUMBERS USING <u>FOR LOOP</u></h1>");
for (i = 1; i <= 10; i++) {
  document.write("<br />" + i);
}

//while loop code to display 1 t0 10 numbers
document.write("<h1>PRINT 1 T0 10 NUMBERS USING <u>WHILE LOOP</u></h1>"+"<br />")
var j = 1;
while (j <= 10) {
  document.write(j + "<br /> ");
  j++;
}

//do while loop code to display 1 t0 10 numbers
document.write("<h1>PRINT 1 T0 10 NUMBERS USING <u>DO WHILE LOOP</u></h1>"+"<br />")
var k=1;
do {
  document.write(k+"<br />");
  k++;
} while(k <= 10);
